export function generateExportContent(formatId, kit, brandDNA = null) {
  const archetype = brandDNA?.aestheticSignature?.archetype ?? 'The Refined Technologist'
  const tagline = brandDNA?.aestheticSignature?.tagline ?? 'Precision meets warmth'
  const description = brandDNA?.aestheticSignature?.description ?? 'Swiss design precision with contemporary digital warmth.'
  const keywords = (brandDNA?.aestheticSignature?.keywords ?? ['precision', 'clarity', 'depth', 'craft', 'intention', 'restraint']).join(', ')
  const style = brandDNA?.meta?.dominantStyle ?? 'Dark Minimalism'
  const confidence = brandDNA?.meta?.confidenceScore ?? 87
  switch (formatId) {
    case 'claude':
      return `# Brand Guidelines — ${kit.name}
> Generated by Tastestack on ${kit.createdAt}

---

## Color Palette

| Role | Hex | Name | Usage |
|------|-----|------|-------|
${kit.colors.map(c => `| ${c.role} | \`${c.hex}\` | ${c.name} | ${c.usage} |`).join('\n')}

---

## Typography

- **Headings:** ${kit.typography.heading.family} ${kit.typography.heading.weight}
- **Body:** ${kit.typography.body.family} ${kit.typography.body.weight}
- **Monospace:** ${kit.typography.mono.family} ${kit.typography.mono.weight}
- **Type Scale:** ${kit.typography.scale}

### Samples
> Heading: "${kit.typography.heading.sample}"
> Body: "${kit.typography.body.sample}"
> Mono: \`${kit.typography.mono.sample}\`

---

## Tone of Voice

**Voice descriptors:** ${kit.toneOfVoice.descriptors.join(', ')}

**Avoid:** ${kit.toneOfVoice.avoid.join(', ')}

### Examples
${kit.toneOfVoice.examples.map(e => `- **${e.type === 'good' ? '✅ Do' : '❌ Don\'t'}:** "${e.text}"`).join('\n')}

---

## Brand Essence

- **Archetype:** ${archetype}
- **Tagline:** ${tagline}
- **Keywords:** ${keywords}
- **Aesthetic:** ${style} — ${description}

---

*Exported from Tastestack — Build your brand DNA*
`

    case 'lovable':
      return JSON.stringify({
        $schema: 'https://tastestack.io/schemas/design-tokens/v1',
        name: kit.name,
        version: kit.version,
        colors: Object.fromEntries(
          kit.colors.map(c => [
            c.role.toLowerCase().replace(/\s+/g, '-'),
            { value: c.hex, description: c.usage, name: c.name },
          ])
        ),
        typography: {
          fontFamily: {
            heading: { value: kit.typography.heading.family },
            body: { value: kit.typography.body.family },
            mono: { value: kit.typography.mono.family },
          },
          scale: kit.typography.scale,
        },
        tone: {
          voice: kit.toneOfVoice.descriptors,
          avoid: kit.toneOfVoice.avoid,
        },
      }, null, 2)

    case 'cursor':
      return `---
description: Brand guidelines for this project
globs: **/*.{tsx,ts,jsx,js,css}
alwaysApply: true
---

# Brand Guidelines

## Color System
\`\`\`
${kit.colors.map(c => c.role.padEnd(20) + ': ' + c.hex + ' (' + c.name + ')').join('\n')}
\`\`\`

## Typography
- Heading: ${kit.typography.heading.family} ${kit.typography.heading.weight}
- Body: ${kit.typography.body.family} ${kit.typography.body.weight}
- Monospace: ${kit.typography.mono.family}

## Visual Principles
- Dark minimalism — deep surfaces, strategic color
- Generous whitespace — let elements breathe
- Violet accents for interactivity and emphasis
- High contrast text on dark backgrounds
- Subtle borders, not heavy shadows

## Tone of Voice
Voice: ${kit.toneOfVoice.descriptors.join(' · ')}
Avoid: ${kit.toneOfVoice.avoid.join(' · ')}

## Aesthetic Archetype
${archetype} — ${tagline}.
${description}
`

    case 'figma':
      return JSON.stringify({
        variables: kit.colors.map(c => ({
          name: `Color/${c.role}`,
          type: 'COLOR',
          value: c.hex,
          description: c.usage,
        })).concat([
          { name: 'Font/Heading', type: 'STRING', value: 'Inter Bold' },
          { name: 'Font/Body', type: 'STRING', value: 'Inter Regular' },
          { name: 'Font/Mono', type: 'STRING', value: 'JetBrains Mono' },
        ]),
        collection: { name: kit.name, modes: ['Dark'] },
      }, null, 2)

    case 'raw':
      return JSON.stringify({
        ...kit,
        brandDNA: {
          archetype,
          tagline,
          description,
          aestheticStyle: style,
          confidenceScore: confidence,
          influences: brandDNA?.aestheticSignature?.influences ?? ['Dieter Rams', 'Swiss Design', 'Japanese Minimalism'],
          keywords: brandDNA?.aestheticSignature?.keywords ?? ['precision', 'clarity', 'depth', 'craft', 'intention'],
        },
      }, null, 2)

    default:
      return ''
  }
}
